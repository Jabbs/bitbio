class User < ActiveRecord::Base
  extend FriendlyId
  friendly_id :full_name, use: [:slugged, :history]
  geocoded_by :full_address
  attr_accessible :account_type, :bio, :email, :first_name, :last_name, :organization,
                  :password, :password_confirmation, :phone, :address, :city, :state,
                  :zip, :country, :membership
  has_secure_password
  
  validates :first_name, presence: true, length: { maximum: 30 }
  validates :last_name, presence: true, length: { maximum: 30 }
  validates :organization, presence: true
  validates :bio, presence: true, length: { minimum: 30 }
  validates :country, presence: true
  validates :email, presence: true, uniqueness: true
  validates :password, presence: true, length: { minimum: 6 }, on: :create
  validates :password_confirmation, presence: true, on: :create
  after_validation :geocode, :if => :address_changed?
  after_validation :geocode, :if => :city_changed?
  after_validation :geocode, :if => :state_changed?
  after_validation :geocode, :if => :zip_changed?
  after_validation :geocode, :if => :country_changed?
  
  before_save { self.email.strip.downcase! }
  before_save { self.first_name.strip.capitalize! }
  before_save { self.last_name.strip.capitalize! }
  before_create { generate_token(:auth_token) }
  
  has_many :sent_messages, foreign_key: "sender_id", class_name: "Message"
  has_many :received_messages, foreign_key: "receiver_id", class_name: "Message"

  has_many :message_receivers, through: :sent_messages, source: :receiver
  has_many :message_senders, through: :received_messages, source: :sender
  
  has_many :projects, dependent: :destroy
  has_many :comments
  
  def contacts
    (self.message_receivers + self.message_senders).uniq
  end
  
  def researcher?
    account_type == "Researcher" ? true : false
  end
  
  def provider?
    account_type == "Provider" ? true : false
  end
  
  def full_address
    "#{address}, #{city}, #{state} #{zip}"
  end
  
  def full_address_present?
    if address? && city? && state? && zip?
      true
    else
      false
    end
  end
  
  def generate_token(column)
    begin
      self[column] = SecureRandom.urlsafe_base64
    end while User.exists?(column => self[column])
  end
  
  def full_name
    self.first_name + " " + self.last_name
  end
  
  def full_name_w_account_type
    self.full_name + " - " + self.account_type
  end
  
